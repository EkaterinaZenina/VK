package domain;

public class LikesInfo {
    private int allLikes;
    private int canLike;

    public int getAllLikes() {
        return allLikes;
    }

    public void setAllLikes(int allLikes) {
        this.allLikes = allLikes;
    }

    public int getCanLike() {
        return canLike;
    }

    public void setCanLike(int canLike) {
        this.canLike = canLike;
    }
}
