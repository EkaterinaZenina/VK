package domain;

public class RepostsInfo {
    private int allReposts;
    private int canRepost;

    public int getAllReposts() {
        return allReposts;
    }

    public void setAllReposts(int allReposts) {
        this.allReposts = allReposts;
    }

    public int getCanRepost() {
        return canRepost;
    }

    public void setCanRepost(int canRepost) {
        this.canRepost = canRepost;
    }
}

